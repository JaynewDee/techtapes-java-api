package com.techtapes;

public class VariablesPractice {
    public static void main(String[] args) {
        int pizzas = 3;
        double eaten = 1.5;
        String animal = "cat";

        double remainder = pizzas - eaten;

        String concatenation = pizzas + animal;
        System.out.println(remainder);
        System.out.println(concatenation);
    }

}
